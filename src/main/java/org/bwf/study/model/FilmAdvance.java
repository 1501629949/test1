package org.bwf.study.model;

public class FilmAdvance {
    private String adv_link_url;
    private String adv_id;
    private String adv_img;
    private String adv_title;
    private String adv_watch_count;

    public String getAdv_link_url() {
        return adv_link_url;
    }

    public void setAdv_link_url(String adv_link_url) {
        this.adv_link_url = adv_link_url;
    }

    public String getAdv_id() {
        return adv_id;
    }

    public void setAdv_id(String adv_id) {
        this.adv_id = adv_id;
    }

    public String getAdv_img() {
        return adv_img;
    }

    public void setAdv_img(String adv_img) {
        this.adv_img = adv_img;
    }

    public String getAdv_title() {
        return adv_title;
    }

    public void setAdv_title(String adv_title) {
        this.adv_title = adv_title;
    }

    public String getAdv_watch_count() {
        return adv_watch_count;
    }

    public void setAdv_watch_count(String adv_watch_count) {
        this.adv_watch_count = adv_watch_count;
    }
}
