package org.bwf.study.model;

public class FilmActorContent {
    private String act_img;
    private String act_name;
    private String act_id;
    private String act_role;

    public String getAct_img() {
        return act_img;
    }

    public void setAct_img(String act_img) {
        this.act_img = act_img;
    }

    public String getAct_name() {
        return act_name;
    }

    public void setAct_name(String act_name) {
        this.act_name = act_name;
    }

    public String getAct_id() {
        return act_id;
    }

    public void setAct_id(String act_id) {
        this.act_id = act_id;
    }

    public String getAct_role() {
        return act_role;
    }

    public void setAct_role(String act_role) {
        this.act_role = act_role;
    }
}
